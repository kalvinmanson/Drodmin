     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //validation
        $this->validate($request, [
            'name'          =>  'required',
            'menu_id'       =>  'required',
            'country'       =>  'required'
        ]);
        
        //save record
        $link = request()->all();
        Link::create($link);

        //response
        return redirect()->action('MenuController@edit', $link['menu_id']);
    }

